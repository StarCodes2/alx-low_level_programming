/**
 * op_add - adds two numbers
 * @a: contains the first number
 * @b: contains the second number
 *
 * Return: returns the result of the addition
 */

int op_add(int a, int b)
{
	return (a + b);
}

/**
 * op_sub - Computes the difference of two numbers
 * @a: contains the first number
 * @b: contains the second number
 *
 * Return: returns the difference of the two numbers
 */

int op_sub(int a, int b)
{
	return (a - b);
}

/**
 * op_mul - Multiply's two numbers
 * @a: contains the first number
 * @b: contains the second number
 *
 * Return: returns the product of the two numbers
 */

int op_mul(int a, int b)
{
	return (a * b);
}

/**
 * op_div - Division of a by b
 * @a: contains the first number
 * @b: contains the second number
 *
 * Return: returns the result of the division
 */

int op_div(int a, int b)
{
	return (a / b);
}

/**
 * op_mod - module division of a by b
 * @a: contains the first number
 * @b: contaims the second number
 *
 * Return: returns the remainder of the division
 */

int op_mod(int a, int b)
{
	return (a % b);
}
